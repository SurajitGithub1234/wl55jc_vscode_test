/**
 * @name : fnKernelGetTickCount
 * @param : NULL
 * @retval : void
 * @details : this function handles the tick count overflow of the api: osKerbelGettickCount, 
 *           typically the tick count value is 32 bit and will overflow after 2^32 ticks which is roughly 49 days @1ms
 * 
 */
uint64_t fnKernelGetTickCount(void) 
{
  static uint32_t tick_h = 0U;
  static uint32_t tick_l = 0U;
  uint32_t tick;
  tick = osKernelGetTickCount();
  if (tick < tick_l) {
    tick_h++;
  }
  tick_l = tick;
  return (((uint64_t)tick_h << 32) | tick_l);
}

